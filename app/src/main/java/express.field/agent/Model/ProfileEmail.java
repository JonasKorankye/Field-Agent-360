package express.field.agent.Model;

public class ProfileEmail {

    private Object emailId;
    private String actorId;
    private String value;
    private String frontEndRecordId;
    private String emailTypeId;
    private String statusId;
    private Object oldValues;
    private Object isPrimary;

    public Object getEmailId() {
        return emailId;
    }

    public void setEmailId(Object emailId) {
        this.emailId = emailId;
    }

    public String getActorId() {
        return actorId;
    }

    public void setActorId(String actorId) {
        this.actorId = actorId;
    }

    public String getValue() {
        return value;
    }

    public void setValue(String value) {
        this.value = value;
    }

    public String getFrontEndRecordId() {
        return frontEndRecordId;
    }

    public void setFrontEndRecordId(String frontEndRecordId) {
        this.frontEndRecordId = frontEndRecordId;
    }

    public String getEmailTypeId() {
        return emailTypeId;
    }

    public void setEmailTypeId(String emailTypeId) {
        this.emailTypeId = emailTypeId;
    }

    public String getStatusId() {
        return statusId;
    }

    public void setStatusId(String statusId) {
        this.statusId = statusId;
    }

    public Object getOldValues() {
        return oldValues;
    }

    public void setOldValues(Object oldValues) {
        this.oldValues = oldValues;
    }

    public Object getIsPrimary() {
        return isPrimary;
    }

    public void setIsPrimary(Object isPrimary) {
        this.isPrimary = isPrimary;
    }

}
